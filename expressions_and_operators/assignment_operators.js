// Operadores de atribuição (Assignment)
let x

// assignment
x = 5
console.log(x)

// addition assignment
x += 2
console.log(x)

// subtraction assignment
x -= 2
console.log(x)

// multiplication assignment
x *= 2
console.log(x)

// division assignment
x /= 2
console.log(x)

// remainder
x %= 2
console.log(x)

// exponentiation
x **= 2
console.log(x)
